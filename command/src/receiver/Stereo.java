public class Stereo {
    public void on() {
        System.out.println("Stereo is on");
    }

    public void setCD() {
        System.out.println("setting cd");
    }

    public void setVolume(int volume) {
        System.out.println("setting volume "+volume);
    }

    public void off() {
        System.out.println("Stereo is off.");
    }
}
